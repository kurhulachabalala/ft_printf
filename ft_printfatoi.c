#include "ft_printf.h"

int		ft_atoi_f(char **s)
{
	int		res;
	int		i;
	int		var;

	res = 0;
	i = 0;
	var = 1;
	while ('0' <= (*s)[i] && (*s)[i] <= '9')
	{
		res = res * 10 + (*s)[i] - '0';
		i++;
	}
	(*s) = &(*s)[i];
	return (res * var);
}
